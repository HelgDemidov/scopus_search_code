# Утилиты для работы с БД — экранирование, хелперы запросов


def escape_ilike(s: str) -> str:
    # Экранируем спецсимволы PostgreSQL ILIKE: \, %, _
    # Порядок важен: \ должен экранироваться первым, иначе двойное экранирование
    return s.replace("\\", "\\\\").replace("%", "\\%").replace("_", "\\_")
