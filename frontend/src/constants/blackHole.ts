// Геометрия сигнатурного эффекта error-страниц (docs/error-experience/spec.md).
// Абсолютные единицы (см) из ТЗ трактуются как относительные — браузер не
// даёт надёжного физического PPI, поэтому длина дуги задаётся в долях
// орбиты чёрной дыры, что даёт то же визуальное соотношение на любом экране.

export const BLACK_HOLE_DIAMETER_RATIO = 0.03; // диаметр = 3% диагонали экрана

// Положение чёрной дыры на error-страницах — общее для NotFoundPage и
// RouteErrorPage (единственный источник, чтобы не разъезжались при правках).
// xRatio=0.70 смещён влево от условного центра правого нижнего квадранта
// экрана (0.75) — осознанно не в центре квадранта и не строго на диагонали
// центр-экрана→центр-квадранта (там смотрелось бы как формальная симметрия).
// yRatio=0.50 — подтянута к вертикальному центру вслед за тем, как панель
// сообщения об ошибке переехала почти к самой шапке сайта (см. ErrorPanel),
// освободив вертикальное пространство; x всё ещё держит дыру правее центра,
// поэтому позиция не читается как «дыра в центре экрана».
export const BLACK_HOLE_POSITION = { xRatio: 0.70, yRatio: 0.50 };

// Пороги — в диаметрах чёрной дыры, отсчёт от ПОВЕРХНОСТИ (не от центра).
// Zone 1 [OUTER..FADE_START]: мягкое вытягивание эллипса вдоль орбиты —
// калибровочная точка ТЗ (−50%/+300%) сидит на границе OUTER. Zone 2
// [0..OUTER]: «кольцо фотонной сферы» — единая зона до самой поверхности
// (заменяет прежние отдельные под-зоны «сильнее вытянутый эллипс» и «дуга
// фиксированной длины»), см. computeLensing.
//
// OUTER = 25% от FADE_START (раунд 3 доработки: «звёзды должны окончательно
// превращаться в линии на внутренних 25% радиуса гравитационного эффекта,
// плавно и всё более выраженно растягиваясь эллипсом за пределами этих
// 25%») — раньше был отдельный порог 1.5D (50% от FADE_START), из-за чего
// заметная деформация начиналась уже за 1–2 диаметра от поверхности,
// заметно раньше желаемого. Граница выражена через FADE_START, а не
// самостоятельной константой — самодокументируется как «четверть общего
// радиуса эффекта» и не может рассинхронизироваться с ним.
export const LENSING_FADE_START_DIAMETERS = 3.0; // дальше — совсем без искажения
export const LENSING_OUTER_DIAMETERS = LENSING_FADE_START_DIAMETERS * 0.25;

// Кольцевая зона [0..OUTER] — радиус и угловой охват дуги как функции
// дистанции до поверхности (см. computeLensing). Радиус кольца стягивается
// к краю диска, а не к реальной дистанции объекта — так выглядят настоящие
// фотонные кольца (Interstellar/Gargantua, EHT M87*): почти фиксированный
// радиус чуть за горизонтом тени, независимо от истинного расстояния
// источника света, который туда «доставил» этот луч.
export const RING_EDGE_RADIUS_FACTOR = 1.15; // радиус кольца = во столько раз больше bh.radius
export const RING_SPAN_AT_OUTER = 0.03; // доля окружности на границе OUTER — продолжение +300% эллипса
export const RING_SPAN_AT_SURFACE = 0.94; // доля окружности вплотную к горизонту — «почти полное кольцо»

// «Сопротивление» деформации — реформирует прогресс t внутри зон (0..1)
// через t^power ПЕРЕД тем, как он идёт в lerp: при power<1 t^power держится
// ближе к «недеформированному» концу дольше, отдавая всю резкую часть
// перехода последнему отрезку перед самой поверхностью — начало и конец
// каждой зоны (калибровочные точки ТЗ) не меняются, т.к. 0^p=0 и 1^p=1 при
// любом p>0. См. computeLensing. Используется ТОЛЬКО для курсора — звёзды
// используют p=1 (без реформирования): их калибровка сделана через саму
// границу зоны OUTER (см. выше, «раунд 3»), а не через кривую сопротивления
// внутри неё — раньше (раунд 2) был отдельный STAR_RESISTANCE_POWER=0.27,
// сейчас избыточен и удалён, чтобы не накладывать два независимых способа
// «замедления» друг на друга.
//
// Курсору задано на 50–60% больше «сопротивления» деформации, чем у звёзд
// (у звёзд p=1 после раунда 3) — тот же порог начала эффекта, но линия/
// кольцо на орбите формируется заметно ближе к горизонту. cbrt (p=1/3)
// даёт на середине зоны ~55% меньше деформации, чем при p=1.
export const CURSOR_RESISTANCE_POWER = 1 / 3;

// Возле самого горизонта (внутри OUTER — вся кольцевая зона [0..OUTER])
// стало визуально слишком ярко из-за плотности перекрывающихся дуг.
// Приглушаем яркость звёзд в этой зоне, а не сокращаем их число — иначе
// кольцо стало бы дырявым/патчистым (дуги перестали бы визуально
// сливаться в сплошную линию).
export const INNER_ZONE_BRIGHTNESS_FACTOR = 0.7;

export const CAPTURED_METEOR_FADE_MS = 250; // как долго видна дуга схлопнувшегося метеора

// Метеоры — быстрые, транзиентные объекты; в отличие от курсора/звёзд им
// оставлен прежний (более узкий) порог «схлопывания» в дугу, не расширенная
// вместе с ним зона п.1.1 — иначе метеор терял бы свой обычный след слишком
// рано, на подлёте, что визуально ломает саму идею «пролетающей звезды».
export const METEOR_CAPTURE_DIAMETERS = 0.5;

// Вихревое вращение звёзд рядом с горизонтом (docs/error-experience/spec.md,
// п.1.2) — начинается там же, где стартует деформация формы (FADE_START =
// 3D), а не на границе кольцевой зоны OUTER (уже ближе к поверхности,
// см. выше): иначе получался бы заметный на глаз порог — «эллипс без
// движения» → «внезапно завертелось».
// На внешнем крае звёзды почти не меняют форму, но уже слегка вращаются;
// к горизонту вращение и деформация нарастают вместе, без разрыва.
// Профиль скорости немонотонный: разгон при приближении (кеплеровское
// ускорение — орбитальная скорость реально растёт ближе к дыре), затем
// резкое торможение почти до нуля у самой поверхности (гравитационное
// замедление времени: dτ/dt = √(1 − r_s/r) → 0 при r → r_s — эффект
// «замороженной звезды», удалённый наблюдатель никогда не видит, как
// объект пересекает горизонт). Пик — заметно ближе к горизонту, чем к
// внешнему краю, и достаточно высокий, чтобы читаться как «в разы быстрее»
// на подлёте, прежде чем затормозиться до нуля в последний момент.
export const ROTATION_OMEGA_MAX = 9.0; // рад/с — пиковая угловая скорость

// Выделенное скопление звёзд у горизонта (docs/error-experience/spec.md,
// п.1.2) — плотный, неправильной формы «фон» для эффекта воронки, а не
// точная симуляция аккреционного диска (WebGL/шейдеры вне scope). Масштаб —
// ~30% площади экрана (VORTEX_RADIUS_RATIO — характерный радиус скопления
// как доля диагонали, не радиуса дыры: иначе туманность была бы жёстко
// привязана к крошечному размеру самой дыры и оставалась тонкой каёмкой
// вокруг неё). Дыра — не в центре туманности, а смещена внутри неё.
export const VORTEX_RADIUS_RATIO = 0.20; // ×диагональ экрана ≈ круг площадью ~30% экрана (16:9)
// 600→900 и 7→10 после визуальной проверки: туманность читалась как узкая
// кайма только вокруг самого эффекта, а не как выраженный фон на всю
// заявленную площадь (~30% экрана) — увеличено фактическое число тусклых
// звёзд/сгущений, сам радиус (VORTEX_RADIUS_RATIO) не менялся.
export const VORTEX_STAR_COUNT = 900;
export const VORTEX_BLOB_COUNT = 10; // сколько смещённых «сгущений» формируют неправильную форму

// Дрейф курсора к центру (docs/error-experience/spec.md, п.3 доработки,
