// Пересекающая полоса — сильно сплюснутый эллипс через центр диска, но
// толщина НЕ константна по углу (см. bandRadiusAt в StarFieldCanvas.tsx):
// у полюсов она "вспухает" навстречу окаёмке. Асимметрично сверху/снизу —
// намеренно, не баг (раунд 9, живая сверка со скриншотом Interstellar):
// верх — гладкий, касательный нахлёст ("шляпка", как далёкая сторона диска,
// линзированная непрерывным изгибом вокруг фотонной сферы); низ — полоса
// заходит НЕМНОГО ЗА радиус окаёмки и пересекает её под видимым углом (как
// прямое, нелинзированное перекрытие близкой стороны диска с тенью — две
// независимые кривые с разной кривизной, пересекающиеся, а не сливающиеся).
