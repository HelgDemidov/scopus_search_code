// «Сопротивление» деформации — реформирует прогресс t внутри зон (0..1)
// через t^power ПЕРЕД тем, как он идёт в lerp: при power<1 t^power держится
// ближе к «недеформированному» концу дольше, отдавая всю резкую часть
// перехода последнему отрезку перед самой поверхностью — начало и конец
// каждой зоны (калибровочные точки ТЗ) не меняются, т.к. 0^p=0 и 1^p=1 при
// любом p>0. См. computeLensing. Используется ТОЛЬКО для курсора — звёзды
// используют p=1 (без реформирования): их калибровка сделана через саму
// границу зоны OUTER (см. выше, «раунд 3»), а не через кривую сопротивления
// внутри неё — раньше (раунд 2) был отдельный STAR_RESISTANCE_POWER=0.27,
// сейчас избыточен и удалён, чтобы не накладывать два независимых способа
// «замедления» друг на друга.
//
// Курсору задано на 50–60% больше «сопротивления» деформации, чем у звёзд
// (у звёзд p=1 после раунда 3) — тот же порог начала эффекта, но линия/
