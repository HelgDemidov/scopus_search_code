// Вкрапления цвета пламени (раунд 9, вторая живая правка) — не одна точка
// фиксированного радиуса, а кластер из нескольких микро-точек неправильной
// формы со смещением от идеальной геометрии ("протуберанец") — имитирует
// хаотичные, асинхронные вспышки газа у горизонта. Позиция/альфа/форма
// кластера пересчитываются заново каждый кадр (persistence сознательно не
// нужна, см. чат) — при animate=false (reduced-motion) вкрапление всегда
// ровно одна точка без смещения, детерминировано.
//
// Третья живая правка — плотность вкраплений считалась в фиксированных px,
// но bh.radius на типичном вьюпорте ~18-20px: кластер шириной ~1.5-2px
// (микро-точка + разброс) занимает ПОЧТИ ВСЮ толщину ремешка (~2px),
// 10 таких кластеров на короткой видимой длине ремешка визуально съедали
// белую заливку целиком — читалось как "ремешок рассыпался на пиксели",
// хотя сама заливка была сплошной. Радиус/разброс — доля bh.radius, не
// фиксированный px, чтобы плотность оставалась разумной на любом размере.
