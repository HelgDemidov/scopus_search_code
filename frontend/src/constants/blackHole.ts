//
// Правка после раунда живой проверки (13.1→13.2): единая параметрическая
// формула (общий купол-контур на пояс+дугу, "ремешок" толщины, сходящиеся
// остриями границы) провалилась — верхняя дуга оставалась клином (купол
// поднимался над тенью всего на 2px), пояс не пересекал тень поперёк
// (плоская зона пояса начиналась только ЗА пределами купольной ширины,
// которая сама шире радиуса тени — то есть в диапазоне x∈[-R,+R], где сидит
// сама тень, пояс структурно не мог появиться), а нижняя дуга схлопывалась
// в "точку" (внутренняя граница была плоской). Вместо починки той же формулы
// — три НЕЗАВИСИМЫЕ фигуры, каждая по простой формуле, нагло перекрывающие
// друг друга по краям (без точной стыковки):
//
// 1. Пояс — отдельная линза (профиль sqrt(1-(x/rx)²), половина эллипса),
//    пересекает тень поперёк по центру, рисуется ПОВЕРХ тени и ПОВЕРХ обеих
//    дуг — прячет швы по бокам.
// 2. Верхняя дуга — полукольцо между окружностью тени (R, внутренняя
//    граница) и куполом, гладко (smoothstep, нулевая производная) сливающимся
//    с ВЕРХНИМ краем пояса на боках — единственный из двух стыков, где нужно
//    именно тангенциальное слияние без излома (см. правку 13.3 ниже).
// 3. Нижняя дуга — то же самое, но толщина убывает к бокам РЕЗКО (линейно,
//    под углом), а не гладко — угол на стыке там желателен, не баг (см. 13.3).
//
// Геометрия задаётся в ЛОКАЛЬНЫХ координатах (x поперёк пояса, y поперёк
// его толщины/окружности, центр — bh), затем каждая точка поворачивается на
// DISK_TILT_RAD вокруг центра дыры (жёсткое тело, тот же cos/sin, что и
// раньше) — тень/кольцо в повороте не участвуют (окружность вокруг своего
// центра инвариантна к повороту). "Полюс" фигуры — ВСЕГДА локальный x=0 (это
// внутренняя точка самого диска, а не экранная верхняя/нижняя точка после
// поворота — поворот жёсткий, толщина/форма считаются ДО него).
export const DISK_RX_FACTOR = 2.2; // × bh.radius — половина ширины пояса (по X, до поворота)
export const DISK_BELT_HALF_THICKNESS_FACTOR = 0.12; // × bh.radius — толщина пояса в центре (x=0)

// Правка 13.3 (живая проверка коммита с тремя независимыми фигурами) — по
// вводной пользователя, три отдельных исправления:

// (а) Пояс — не строго прямой, а прогнут вниз, гладкий симметричный
// полуэллипс (не "чайка"/излом в центре — уточнено у пользователя явно).
// Профиль — та же sqrt(1-(x/rx)²), что и толщина — ноль на остриях,
// максимум в центре (там же, где сама тень). Глубина — 10% от радиуса
// ТЕНИ (R), НЕ от длины пояса: первая прикидка (10% от 2×rx=0.44×R)
// физически конфликтовала с толщиной нижней дуги — у бокового края тени
// (x≈R) прогнутый пояс уходил вниз на ~0.5×R, а нижняя дуга по спецификации
// в этой же точке должна иметь толщину всего ~0.1×R (см. drawDiskLowerArc в
// StarFieldCanvas.tsx — там либо появлялся зазор между дугой и поясом, либо
